class City {
  final int id;
  final String main;
  final String description;
  final String icon;
  final int day;
  final int min;
  final int max;
  final int night;
  final int eve;
  final int morn;
  City({
    required this.id,
    required this.main,
    required this.description,
    required this.icon,
    required this.day,
    required this.min,
    required this.max,
    required this.night,
    required this.eve,
    required this.morn,
  });
}
